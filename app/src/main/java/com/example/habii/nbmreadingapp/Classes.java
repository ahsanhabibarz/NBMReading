package com.example.habii.nbmreadingapp;

public class Classes extends PostID {

    String id,name;

    public Classes(){}

    public Classes(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
